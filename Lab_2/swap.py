_first_var = 10
_second_var = 20
print("Before swapping: ")
print("First variable: ", _first_var)
print("Second variable: ", _second_var)
# Swapping the values
_temp_var = _first_var  
_first_var = _second_var
_second_var = _temp_var
print("After swapping: ")
print("First variable: ", _first_var)
print("Second variable: ", _second_var) 